#---- Base de datos para el reto PyME Bancomer ----#

# date_year
# Año en el que se realizaron las transacciones

# date_month
# Mes en el que se realizaron las transacciones

# date_day
# Día en el que se realizaron las transacciones

# id_afil
# Identificador de la afiliación (número consecutivo - enmascarado)

# ix_tarj_bmr
# Indicadora de emisor Bancomer u otros emisores (Bancomer=1)

# cd_edo_cmr
# Código de entidad federativa en donde se ubica la afiliación

# cd_postal_cmr
# Código postal de la ubicación donde se ubica la afiliación

# im_txn
# Importe facturado por las transacciones

# to_txn
# Total de transacciones

# to_ctes
# Total de clientes que realizaron transacciones (aplica sólo emisor Bancomer)

# to_tarjetas
# Total de tarjetas con las que se realizaron las transacciones
